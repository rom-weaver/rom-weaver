fn alias_map(aliases: &[(&'static str, &'static str)]) -> Value {
    let mut out = Map::new();
    for (alias, canonical) in aliases {
        out.insert(
            (*alias).to_string(),
            Value::String((*canonical).to_string()),
        );
    }
    Value::Object(out)
}

fn unique_formats(groups: &[&[&'static str]]) -> Vec<&'static str> {
    let mut out = Vec::new();
    for group in groups {
        for format in *group {
            if !out.contains(format) {
                out.push(*format);
            }
        }
    }
    out
}
