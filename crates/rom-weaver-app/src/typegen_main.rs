#[derive(Clone, Copy, Debug, Eq, PartialEq)]
enum Mode {
    Write,
    Check,
}

fn main() {
    if let Err(error) = run() {
        eprintln!("error: {error}");
        std::process::exit(1);
    }
}

fn run() -> Result<(), String> {
    let mode = parse_mode(env::args().skip(1).collect())?;
    let outputs = [
        GeneratedOutput {
            path: TYPES_OUTPUT_PATH,
            rendered: strip_trailing_whitespace(render_types()),
        },
        GeneratedOutput {
            path: METADATA_OUTPUT_PATH,
            rendered: strip_trailing_whitespace(render_metadata()),
        },
        GeneratedOutput {
            path: COMMAND_TYPES_OUTPUT_PATH,
            rendered: strip_trailing_whitespace(render_command_types()),
        },
    ];

    match mode {
        Mode::Write => {
            for output in &outputs {
                write_output(output)?;
            }
            Ok(())
        }
        Mode::Check => {
            let mut stale_paths = Vec::new();
            for output in &outputs {
                if check_output(output)? {
                    println!("{} is up to date", output.path);
                } else {
                    stale_paths.push(output.path);
                }
            }
            if stale_paths.is_empty() {
                Ok(())
            } else {
                Err(format!(
                    "generated output is stale in {}; run `mise run typegen`",
                    stale_paths.join(", ")
                ))
            }
        }
    }
}

struct GeneratedOutput {
    path: &'static str,
    rendered: String,
}

fn write_output(output: &GeneratedOutput) -> Result<(), String> {
    let output_path = Path::new(output.path);
    if let Some(parent) = output_path.parent() {
        fs::create_dir_all(parent)
            .map_err(|error| format!("failed to create {}: {error}", parent.display()))?;
    }
    fs::write(output_path, &output.rendered)
        .map_err(|error| format!("failed to write {}: {error}", output_path.display()))?;
    println!("wrote {}", output_path.display());
    Ok(())
}

fn check_output(output: &GeneratedOutput) -> Result<bool, String> {
    let output_path = Path::new(output.path);
    let existing = fs::read_to_string(output_path).map_err(|error| {
        format!(
            "failed to read {} for --check (run with --write first): {error}",
            output_path.display()
        )
    })?;
    Ok(existing == output.rendered)
}

fn parse_mode(args: Vec<String>) -> Result<Mode, String> {
    match args.as_slice() {
        [flag] if flag == "--write" => Ok(Mode::Write),
        [flag] if flag == "--check" => Ok(Mode::Check),
        _ => Err("usage: rom-weaver-typegen --write | --check".to_string()),
    }
}

/// ts-rs renders multi-line struct declarations (structs whose fields carry doc
/// comments) with a trailing space on each line. Strip per-line trailing
/// whitespace from every generated file so they satisfy the repo's
/// `git diff --check` rule. Applied to both `--write` and `--check`, so the
/// output stays byte-stable between them.
fn strip_trailing_whitespace(rendered: String) -> String {
    let mut out = String::with_capacity(rendered.len());
    for segment in rendered.split_inclusive('\n') {
        match segment.strip_suffix('\n') {
            Some(line) => {
                out.push_str(line.trim_end());
                out.push('\n');
            }
            None => out.push_str(segment.trim_end()),
        }
    }
    out
}

